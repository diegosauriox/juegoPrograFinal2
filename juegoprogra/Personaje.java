package juegoprogra;
public class Personaje {
protected int atk;
protected int def;
protected float hp;
protected int spd;
protected String faccion;

public int getAtk() {
	return atk;
}
public void setAtk(int atk) {
	this.atk = atk;
}
public int getDef() {
	return def;
}
public void setDef(int def) {
	this.def = def;
}
public float getHp() {
	return hp;
}
public void setHp(float hp) {
	this.hp = hp;
}
public int getSpd() {
	return spd;
}
public void setSpd(int spd) {
	this.spd = spd;
}
public String getFaccion() {
	return faccion;
}
public void setFaccion(String faccion) {
	this.faccion = faccion;
}

}
